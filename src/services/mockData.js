// 隨機選擇指定難度的3張卡片
export const getRandomMockCards = (difficulty) => {
  const cards = mockCards[difficulty] || [];
  if (cards.length < 3) {
    throw new Error(`${difficulty}難度的卡片數量不足3張`);
  }
  
  // 隨機選擇3張不重複的卡片
  const shuffled = [...cards].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, 3);
};

// 檢查是否有足夠的卡片
export const checkCardAvailability = (difficulty) => {
  const cards = mockCards[difficulty] || [];
  return cards.length >= 3;
}; 